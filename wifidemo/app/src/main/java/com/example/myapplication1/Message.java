package com.example.myapplication1;


public class Message {
    public String ip;
    public int port;
    public String msg;

    public String getIp() {
        return ip;
    }

    public int getPort() {
        return port;
    }

    public String getMsg() {
        return msg;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public void setPort(int port) {
        this.port = port;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
